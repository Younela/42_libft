char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	s;

	s = (char)c;
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)s + i);
		i++;
	}
	return ((char *)s);
}
